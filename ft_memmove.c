#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t size)

{
	char	*buff;

	buff = (char *)malloc(size * sizeof(char) + 1);
	if (!buff)
		return (0);
	memcpy(buff, src, size);
	memcpy(dest, buff, size);
	free(buff);
	return (dest);
}

