#include "libft.h"

int	ft_atoi(const char *a)
{
	int	ret;
	int	sign;

	ret = 0;
	sign = 1;
	while ((*a >= 9 && *a <= 13) || *a == 32)
	{
		a++;
	}
	if (*a == '+' || *a == '-')
	{
		if (*a == '-')
			sign = -1;
		a++;
	}
	while (*a >= '0' && *a <= '9')
	{
		ret = ret * 10 + (*a - '0');
		a++;
	}
	return (sign * ret);
}
